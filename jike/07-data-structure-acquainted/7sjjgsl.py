#Python内置的数据结构有元组、列表、字典等


#现在有三个物品，分别是“apple”，“orange”，“pear”，需要将这三个物品存储起来


#存储方式一：这三个物品每个物品按顺序分别存储到一个柜子中，这些物品可以取出来，如下所示
["apple","orange","pear"]



#存储方式二：这三个物品每个物品按顺序分别存储到一个柜子中，这些物品不可以取出来，也不可以放新物品跟其挤一个柜子，如下所示
("apple","orange","pear")


#存储方式三：这三个物品不仅按顺序分别存储到一个柜子中，而且每个柜子还得有个名称
{"sam":"apple","Jac":"orange","mating":"pear"}
